    /**
     * Helper to build simple HTML content blocks.
     */
    private function articleContent(array $sections): string
    {
        $html = '';
        foreach ($sections as $title => $value) {
            $html .= "<h2>{$title}</h2>";
            if (is_array($value)) {
                $html .= '<ul>';
                foreach ($value as $item) {
                    $html .= "<li>{$item}</li>";
                }
                $html .= '</ul>';
            } else {
                $html .= "<p>{$value}</p>";
            }
        }
        return $html;
    }
